package Object_Oriented_Program.Data;

public class Calculator {

    // Method Penjumlahan
    public Integer add(Integer x, Integer y) {
        Integer hasil = x + y;
        return hasil;
    }

    // Method Pengurangan
    public Integer substract(Integer x, Integer y) {
        Integer hasil = x - y;
        return hasil;
    }

    // Method Perkalian
    public Integer multiply(Integer x, Integer y) {
        Integer hasil = x * y;
        return hasil;
    }

    // Method Pembagian
    public Integer divide(Integer x, Integer y) {
        Integer hasil = x / y;
        return hasil;
    }
}

